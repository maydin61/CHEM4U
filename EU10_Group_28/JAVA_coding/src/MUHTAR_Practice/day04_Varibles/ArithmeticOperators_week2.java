package JAVA_coding.src.MUHTAR_Practice.day04_Varibles;

public class ArithmeticOperators_week2 {
    public static void main(String[] args) {

        System.out.println(20.5*12);
        System.out.println((float) 15/4); // 3.75
        System.out.println(34-6+10); // 38
        System.out.println(234+2344f/9);
        System.out.println("Remainder of # division result after the value is; "
                + 200 % 3);    // % cal of remainder, rest
        System.out.println(20/6f); //3.333....6f(f stand for float)
    }

}

