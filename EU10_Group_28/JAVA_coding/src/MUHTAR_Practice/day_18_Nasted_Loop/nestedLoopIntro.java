package JAVA_coding.src.MUHTAR_Practice.day_18_Nasted_Loop;

public class nestedLoopIntro {
    public static void main(String[] args) {
        for (int i = 0; i <=10; i++) {
            System.out.println("");
            for (int j = 0; j <= 10; j++) {
                System.out.print("* ");
            }
        }
    }
}
