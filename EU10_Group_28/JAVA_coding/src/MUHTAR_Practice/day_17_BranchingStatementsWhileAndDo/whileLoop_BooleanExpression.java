package JAVA_coding.src.MUHTAR_Practice.day_17_BranchingStatementsWhileAndDo;

public class whileLoop_BooleanExpression {
    public static void main(String[] args) {
        if (true) {                               // happen only if is true
            System.out.println("Wooden Spoon");
        }
        System.out.println("============");
        int number=0;
        while (number<=5){                        // repeated if statement
            System.out.print("Wooden Spoon ");
            number++;
        }



    }
}