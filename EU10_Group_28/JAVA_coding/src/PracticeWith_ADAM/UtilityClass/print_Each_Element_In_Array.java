package JAVA_coding.src.PracticeWith_ADAM.UtilityClass;

public class print_Each_Element_In_Array {
    public static void main(String[] args) {

    }
    // print each integer of an array in separated lines
    public static void printEachElement(int [] array){
        for (int each:array){
            System.out.println(each);
        }
    }
    // print each double of an array in separated lines
    public static void printEachElement(double [] array){
        for (double each:array){
            System.out.println(each);
        }
    }
    // print each char of an array in separated lines
    public static void printEachElement(char [] array){
        for (char each:array){
            System.out.println(each);
        }
    }
    // print each String of an array in separated lines
    public static void printEachElement(String [] array){
        for (String each:array){
            System.out.println(each);
        }
    }
}
