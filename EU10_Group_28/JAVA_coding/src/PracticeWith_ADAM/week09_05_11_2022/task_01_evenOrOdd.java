package JAVA_coding.src.PracticeWith_ADAM.week09_05_11_2022;

public class task_01_evenOrOdd {
    public static void main(String[] args) {

            checkNumberEvenOrOdd(5);
            checkNumberEvenOrOdd(12);
            checkNumberEvenOrOdd(20);






    }

    private static void checkNumberEvenOrOdd(int number) {
        if (number%2==0){
            System.out.println(number+ ": is even");
        }else {
            System.out.println(number+": is odd");
        }
    }



    }

